package com.example.belgitask;

public class Task {
    private string name;
    private string detail;
    private int photo;

    public int getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getPhoto() {
        return photo;
    }

    public void setPhoto(int photo) {
        this.photo = photo;
    }
}
